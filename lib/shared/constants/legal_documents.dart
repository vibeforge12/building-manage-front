회원이 서비스 내에 게시한 게시물의 저작권은 해당 회원에게 귀속됩니다.

2. 게시물의 삭제
회사는 다음 각 호에 해당하는 게시물을 사전 통지 없이 삭제하거나 비공개 처리할 수 있습니다.
• 다른 회원 또는 제3자를 비방하거나 명예를 손상시키는 내용
• 공공질서 및 미풍양속에 위반되는 내용
• 범죄적 행위에 결부된다고 인정되는 내용
• 회사의 저작권, 제3자의 저작권 등 권리를 침해하는 내용
• 회사가 규정한 게시물 원칙에 어긋나거나 게시판 성격에 부합하지 않는 내용
• 기타 관련 법령에 위반되는 내용


